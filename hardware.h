#ifndef _HARDWARE_H_
#define _HARDWARE_H_

#define SYS_IO_PHY_ADR_BASE	0x20000000

#define	GPIO_PHY_ADR_BASE	(SYS_IO_PHY_ADR_BASE + 0x00200000)
#define GPIO_FSEL_0		(GPIO_PHY_ADR_BASE   + 0x00000000)
#define GPIO_FSEL_1		(GPIO_PHY_ADR_BASE   + 0x00000004)

#define GPIO_SET_0		(GPIO_PHY_ADR_BASE   + 0x0000001c)
#define GPIO_SET_1		(GPIO_PHY_ADR_BASE   + 0x00000020)

#define GPIO_CLR_0		(GPIO_PHY_ADR_BASE   + 0x00000028)
#define GPIO_CLR_1		(GPIO_PHY_ADR_BASE   + 0x0000002c)

#endif
